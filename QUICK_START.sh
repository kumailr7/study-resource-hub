echo "🚀 Starting the Application..."
echo ""
echo "Step 1: Build and start all services"
echo "$ docker-compose up --build"
echo ""
echo "This will:"
echo "   1. Start Vault server on http://localhost:8200"
echo "   2. Initialize Vault with AppRole authentication"
echo "   3. Start backend server on http://localhost:5000"
echo "   4. Start frontend on http://localhost:3000"
echo "   5. Configure nginx reverse proxy"
