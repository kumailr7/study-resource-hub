echo ""

echo "📝 Useful Commands:"
echo ""
echo "View Vault Status:"
echo "$ docker exec vault_server vault status"
echo ""
echo "View Stored Secrets:"
echo "$ docker exec vault_server vault login -method=token token=root"
echo "$ docker exec vault_server vault kv get secret/app/backend"
echo ""
echo "View Backend Logs:"
echo "$ docker logs backend_resource"
echo ""
echo "View Vault Agent Logs:"
echo "$ docker logs backend_resource | grep 'Vault Agent'"
echo ""
echo "Access Backend Container:"
echo "$ docker exec -it backend_resource sh"
echo ""
echo "Verify Secrets Loaded:"
echo "$ docker exec backend_resource cat /vault/secrets/backend.env"
echo ""

echo "🔍 Troubleshooting:"
echo ""
echo "If backend fails to start:"
